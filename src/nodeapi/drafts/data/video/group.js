function get_all_group_Effects(mode=0){
    let tmp_effects =  group.data.CategoryEffects.Effects;
    let vip_effects = []
    let effects = []
    for(let effect of tmp_effects){
        let tmp ={
            "anim_adjust_params": null,
            "category_id": "group",
            "category_name": "组合",
            "duration": 3000000,
            "id": effect.EffectId,
            "material_type": "video",
            "name": effect.Name,
            "panel": "video",
            "path": "",
            "platform": "all",
            "request_id": "",
            "resource_id": effect.ResourceId,
            "start": 1500000,
            "type": "group",
            "icon_url":effect.IconUrl.UrlList[0]
        }
        
        let extra =  JSON.parse( effect.Extra)
        if(extra.is_vip){
            tmp.is_vip = 1
            vip_effects.push(tmp)
        }else{
            tmp.is_vip = 0
            effects.push(tmp)
        }

    }

    if(mode==0 ){
        return effects
    }else if(mode == 1){
        return vip_effects
    }else{
        return [...vip_effects,...effects]
    }
}

function find_group_animation_by_resource_id(resource_id){
    let effects = get_all_group_Effects(2);
    for(let info of effects){
        if(info.resource_id == resource_id){
            return info
        }
    }
    return null

}


