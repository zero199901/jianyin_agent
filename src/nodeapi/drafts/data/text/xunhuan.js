//mode = 0 非vip  mode = 1 vip  mode = 2 all 
function get_all_xunhuanchang_Effects(mode=0){
    let tmp_effects =  xunhuan.data.CategoryEffects.Effects;
    let vip_effects = []
    let effects = []
    for(let effect of tmp_effects){
        let tmp = {
            "anim_adjust_params": null,
            "category_id": "xunhuan",
            "category_name": "循环",
            "duration": 500000,
            "id": effect.EffectId,
            "material_type": "sticker",
            "name": effect.Name,
            "panel": "",
            "path": "",
            "platform": "all",
            "request_id": "",
            "resource_id": effect.ResourceId,
            "start": 0,
            "type": "loop",
            "icon_url":effect.IconUrl.UrlList[0]
        }
        let extra =  JSON.parse( effect.Extra)
        if(extra.is_vip){
            tmp.is_vip = 1
            vip_effects.push(tmp)
        }else{
            tmp.is_vip = 0
            effects.push(tmp)
        }

    }

    if(mode==0 ){
        return effects
    }else if(mode == 1){
        return vip_effects
    }else{
        return [...vip_effects,...effects]
    }
}

function find_xunhuan_animation_by_resource_id(resource_id){
    let effects = get_all_xunhuanchang_Effects(2);
    for(let info of effects){
        if(info.resource_id == resource_id){
            return info
        }
    }
    return null

}


